    private void btnAnalisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalisarActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
       chooser.showOpenDialog(null);
        try {
            Reader leitor = new BufferedReader (new FileReader(chooser.getSelectedFile()));
            RegrasLex lex = new RegrasLex(leitor);
            String Resultado = "";
            while(true){
                Tokens tokens = lex.yylex();
                if(tokens == null){
                    Resultado +="\n\n\t\tFIN - Created by Abubacar";
                    txtResultado.setText(Resultado);
                    return;
                }
                switch(tokens){
                    case ERRO:
                        Resultado +="Simbolo n√£o definido\n";
                    break;
                    case Identificador: case Numero_Inteiro : case Numero_Real: case Palavra_Reservada:
                        Resultado += lex.lexeme +"\t TOKEN: "+ tokens+"\n";
                    break;
                    default:
                        Resultado += lex.yytext() + "\t TOK: "+tokens+"\n";
                        break;
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FormMain.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FormMain.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnAnalisarActionPerformed

